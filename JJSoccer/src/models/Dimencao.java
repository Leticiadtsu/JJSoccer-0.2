
package models;

/**
 *
 * @author 
 */
public class Dimencao {
    
    private final int largura;
    private final int altura;

    public Dimencao(int largura, int altura) {
        this.largura = largura;
        this.altura = altura;
    }

    public int getLargura() {
        return largura;
    }

    public int getAltura() {
        return altura;
    }
    
    
    
    
}
